-- Import one paper. Accepts sections/questions or question_groups.
create or replace function public.import_ielts_paper_json(
  payload jsonb,
  p_suite_id bigint default null
)
returns bigint
language plpgsql
security definer
set search_path=public
as $$
declare
  paper_pk bigint;
  section_pk bigint;
  s jsonb;
  g jsonb;
  q jsonb;
  sn int:=0;
  qn int:=0;
  questions_array jsonb;
  answer_payload jsonb;
begin
  insert into public.ielts_papers(
    suite_id,slug,title,module,test_type,duration_minutes,
    instructions,metadata,status,is_published,created_by,updated_at
  )
  values(
    p_suite_id,
    payload->>'slug',
    payload->>'title',
    payload->>'module',
    coalesce(payload->>'test_type','academic'),
    coalesce((payload->>'duration_minutes')::int,60),
    payload->>'instructions',
    coalesce(payload->'metadata','{}'::jsonb),
    coalesce(payload->'metadata'->>'status','draft'),
    coalesce(payload->'metadata'->>'status','draft')='published',
    auth.uid(),
    now()
  )
  on conflict(slug) do update set
    suite_id=excluded.suite_id,
    title=excluded.title,
    module=excluded.module,
    test_type=excluded.test_type,
    duration_minutes=excluded.duration_minutes,
    instructions=excluded.instructions,
    metadata=excluded.metadata,
    status=excluded.status,
    is_published=excluded.is_published,
    updated_at=now()
  returning id into paper_pk;

  delete from public.ielts_sections where paper_id=paper_pk;

  for s in select * from jsonb_array_elements(coalesce(payload->'sections','[]'::jsonb))
  loop
    sn:=sn+1;

    insert into public.ielts_sections(
      paper_id,section_number,title,passage,audio_url,transcript,instructions,metadata
    )
    values(
      paper_pk,
      coalesce((s->>'section_number')::int,sn),
      s->>'title',
      s->>'passage',
      s->>'audio_url',
      s->>'transcript',
      s->>'instructions',
      coalesce(s->'metadata','{}'::jsonb)
    )
    returning id into section_pk;

    qn:=0;

    if jsonb_typeof(s->'questions')='array' then
      questions_array:=s->'questions';
    elsif jsonb_typeof(s->'question_groups')='array' then
      questions_array:='[]'::jsonb;
      for g in select * from jsonb_array_elements(s->'question_groups')
      loop
        for q in select * from jsonb_array_elements(coalesce(g->'questions','[]'::jsonb))
        loop
          questions_array:=questions_array || jsonb_build_array(
            q || jsonb_build_object(
              'type',coalesce(q->>'type',g->>'type'),
              'group_instructions',g->>'instructions',
              'group_options',g->'options',
              'group_prompt',g->>'prompt'
            )
          );
        end loop;
      end loop;
    else
      questions_array:='[]'::jsonb;
    end if;

    for q in select * from jsonb_array_elements(questions_array)
    loop
      qn:=qn+1;
      answer_payload:=case
        when q ? 'answer' then q->'answer'
        else null
      end;

      insert into public.ielts_items(
        section_id,item_number,question_type,prompt,options,
        correct_answer,explanation,metadata
      )
      values(
        section_pk,
        coalesce((q->>'number')::int,qn),
        coalesce(q->>'type',q->>'question_type','short_answer'),
        coalesce(q->>'prompt',q->>'question',''),
        case
          when jsonb_typeof(q->'options') is not null then q->'options'
          when jsonb_typeof(q->'group_options') is not null then q->'group_options'
          else '[]'::jsonb
        end,
        answer_payload,
        q->>'explanation',
        coalesce(q->'metadata','{}'::jsonb)
          || jsonb_build_object(
            'group_instructions',q->>'group_instructions',
            'group_prompt',q->>'group_prompt'
          )
      );
    end loop;
  end loop;

  return paper_pk;
end;
$$;

-- Accepts either:
-- {suite:{...}, papers:[...]}
-- or {suite:{...}, paper:{...}}
create or replace function public.admin_import_ielts_bundle_json(payload jsonb)
returns jsonb
language plpgsql
security definer
set search_path=public
as $$
declare
  suite_payload jsonb;
  paper_payload jsonb;
  papers_payload jsonb;
  suite_pk bigint;
  paper_pk bigint;
  imported_ids jsonb:='[]'::jsonb;
  paper_count int:=0;
begin
  if auth.uid() is null or not public.is_admin() then
    raise exception 'Administrator access required';
  end if;

  if payload->'suite' is null then
    raise exception 'Missing suite object';
  end if;

  if jsonb_typeof(payload->'papers')='array' then
    papers_payload:=payload->'papers';
  elsif payload->'paper' is not null then
    papers_payload:=jsonb_build_array(payload->'paper');
  else
    raise exception 'Missing paper or papers';
  end if;

  suite_payload:=payload->'suite';

  insert into public.ielts_test_suites(
    slug,title,description,source_filename,status,created_by,metadata,updated_at
  )
  values(
    suite_payload->>'slug',
    suite_payload->>'title',
    suite_payload->>'description',
    suite_payload->>'source_file',
    coalesce(suite_payload->>'status','draft'),
    auth.uid(),
    coalesce(suite_payload,'{}'::jsonb),
    now()
  )
  on conflict(slug) do update set
    title=excluded.title,
    description=excluded.description,
    source_filename=excluded.source_filename,
    status=excluded.status,
    metadata=excluded.metadata,
    updated_at=now()
  returning id into suite_pk;

  for paper_payload in select * from jsonb_array_elements(papers_payload)
  loop
    -- Normalize V3 single reading paper format.
    if paper_payload->>'slug' is null then
      paper_payload:=paper_payload || jsonb_build_object(
        'slug',coalesce(
          suite_payload->>'slug',
          regexp_replace(lower(paper_payload->>'title'),'[^a-z0-9]+','-','g')
        ),
        'module',coalesce(suite_payload->>'module','reading'),
        'test_type',coalesce(suite_payload->>'type','academic'),
        'duration_minutes',coalesce(
          (suite_payload->>'duration_minutes')::int,
          (suite_payload->>'duration')::int,
          60
        ),
        'metadata',jsonb_build_object(
          'status',coalesce(suite_payload->>'status','draft')
        )
      );
    end if;

    paper_pk:=public.import_ielts_paper_json(paper_payload,suite_pk);
    imported_ids:=imported_ids || jsonb_build_array(paper_pk);
    paper_count:=paper_count+1;
  end loop;

  return jsonb_build_object(
    'success',true,
    'suite_id',suite_pk,
    'paper_ids',imported_ids,
    'paper_count',paper_count
  );
end;
$$;

create or replace function public.save_ielts_attempt(
  p_paper_id bigint,
  p_answers jsonb,
  p_flagged_items jsonb default '[]'::jsonb,
  p_section_times jsonb default '{}'::jsonb,
  p_submit boolean default true
)
returns jsonb
language plpgsql
security definer
set search_path=public
as $$
declare
  uid uuid:=auth.uid();
  attempt_pk bigint;
  item_record record;
  supplied jsonb;
  correct_count int:=0;
  total_count int:=0;
  score_value numeric(5,2);
begin
  if uid is null then raise exception 'Not authenticated'; end if;

  for item_record in
    select i.id,i.item_number,i.correct_answer
    from public.ielts_items i
    join public.ielts_sections s on s.id=i.section_id
    where s.paper_id=p_paper_id
  loop
    total_count:=total_count+1;
    supplied:=p_answers->(item_record.item_number::text);

    if item_record.correct_answer is not null
       and lower(trim(both '"' from supplied::text))
           = lower(trim(both '"' from item_record.correct_answer::text))
    then
      correct_count:=correct_count+1;
    end if;
  end loop;

  score_value:=case when total_count>0
    then round(correct_count::numeric*100/total_count,2)
    else null end;

  insert into public.ielts_attempts(
    user_id,paper_id,answers,flagged_items,section_times,
    score,raw_score,total_items,status,submitted_at,updated_at
  )
  values(
    uid,p_paper_id,coalesce(p_answers,'{}'::jsonb),
    coalesce(p_flagged_items,'[]'::jsonb),
    coalesce(p_section_times,'{}'::jsonb),
    score_value,correct_count,total_count,
    case when p_submit then 'submitted' else 'in_progress' end,
    case when p_submit then now() else null end,
    now()
  )
  returning id into attempt_pk;

  return jsonb_build_object(
    'success',true,
    'attempt_id',attempt_pk,
    'raw_score',correct_count,
    'total_items',total_count,
    'score_percent',score_value
  );
end;
$$;

create or replace function public.admin_publish_ielts_suite(p_suite_id bigint)
returns jsonb
language plpgsql
security definer
set search_path=public
as $$
declare
  incomplete_answers int;
  incomplete_audio int;
begin
  if auth.uid() is null or not public.is_admin() then
    raise exception 'Administrator access required';
  end if;

  select count(*) into incomplete_answers
  from public.ielts_items i
  join public.ielts_sections s on s.id=i.section_id
  join public.ielts_papers p on p.id=s.paper_id
  where p.suite_id=p_suite_id
    and p.module in ('reading','listening')
    and i.correct_answer is null;

  select count(*) into incomplete_audio
  from public.ielts_sections s
  join public.ielts_papers p on p.id=s.paper_id
  where p.suite_id=p_suite_id
    and p.module='listening'
    and coalesce(s.audio_url,'')='';

  if incomplete_answers>0 or incomplete_audio>0 then
    return jsonb_build_object(
      'success',false,
      'missing_answers',incomplete_answers,
      'missing_audio_sections',incomplete_audio,
      'message','Complete answer keys and Listening audio before publishing.'
    );
  end if;

  update public.ielts_test_suites
  set status='published',updated_at=now()
  where id=p_suite_id;

  update public.ielts_papers
  set status='published',is_published=true,updated_at=now()
  where suite_id=p_suite_id;

  return jsonb_build_object('success',true,'suite_id',p_suite_id);
end;
$$;

