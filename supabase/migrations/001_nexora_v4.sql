create or replace function public.complete_lesson(
  p_level_number int,
  p_score numeric default 100
)
returns jsonb
language plpgsql
security definer
set search_path=public
as $$
declare
  uid uuid:=auth.uid();
  reward int;
  coin int;
  was_done boolean;
begin
  if uid is null then raise exception 'Not authenticated'; end if;

  select xp_reward,coin_reward
  into reward,coin
  from public.lessons
  where level_number=p_level_number and is_published=true;

  if reward is null then raise exception 'Lesson not found'; end if;

  select completed into was_done
  from public.user_progress
  where user_id=uid and level_id=p_level_number;

  was_done:=coalesce(was_done,false);

  insert into public.user_progress(
    user_id,level_id,completed,score,earned_xp,earned_coins,completed_at,updated_at
  )
  values(
    uid,p_level_number,true,p_score,
    case when was_done then 0 else reward end,
    case when was_done then 0 else coin end,
    now(),now()
  )
  on conflict(user_id,level_id)
  do update set
    completed=true,
    score=excluded.score,
    updated_at=now();

  update public.profiles
  set
    current_lesson=greatest(current_lesson,least(p_level_number+1,100)),
    total_xp=total_xp+case when was_done then 0 else reward end,
    coins=coins+case when was_done then 0 else coin end,
    updated_at=now()
  where id=uid;

  return jsonb_build_object(
    'success',true,
    'xp_awarded',case when was_done then 0 else reward end,
    'coins_awarded',case when was_done then 0 else coin end
  );
end;
$$;

